package exercicios;

public class TabuadaFor {

	
	public static void main(String[] args) {
		
		int tabuadaDe = 5;
		
		
		System.out.println("Valor da Tabuada de: " + tabuadaDe);
		
		for (int i = 1; i < 11 ; i++) {
			
			System.out.println(i +  "x" + tabuadaDe   +  "=" + tabuadaDe * i);
			
		}
		
		
	}
	
	
	
}
